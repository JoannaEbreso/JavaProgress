public class FractionsArray{
    public static void main(String[]args){
        final int arraySize = 10;
        double[] fractions = new double[arraySize];
        fractions[4];
        fractions[9]=1.667;
        fractions[6]=3.333;

        for(x=0; x<=arraySize; x++){
            double total=0.0;
            total+=fractions[x];
        }
    }
}